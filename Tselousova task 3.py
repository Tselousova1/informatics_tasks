# Задача 3. Конвертер температур
# Программа переводит температуру из Цельсия в Фаренгейты
# и определяет состояние воды при этой температуре.

# Температура в градусах Цельсия
celsius = 0

# Перевод в градусы Фаренгейта
fahrenheit = celsius * 9 / 5 + 32

# Определение состояния воды
if celsius <= 0:
    state = "Лёд"
elif celsius >= 100:
    state = "Пар"
else:
    state = "Жидкость"

# Вывод результата
print("=== КОНВЕРТЕР ТЕМПЕРАТУР ===")
print(f"Температура: {celsius}°C")
print(f"В Фаренгейтах: {fahrenheit:.1f}°F")
print(f"Состояние воды: {state}")
