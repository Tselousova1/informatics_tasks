# Задача 6. Каталог материалов
# Программа выполняет основные операции со списком материалов:
# вывод элементов, добавление, удаление и подсчёт длины списка.

# Исходный список материалов
materials = ["Стекло", "Зеркало", "Стеклопакет", "Профиль", "Уплотнитель"]

# Вывод исходных данных
print("=== КАТАЛОГ МАТЕРИАЛОВ ===")
print("Исходный список:", materials)
print(f"Первый элемент: {materials[0]}")
print(f"Последний элемент: {materials[-1]}")
print(f"Средние элементы: {materials[1:4]}")

# Добавление новых материалов
materials.append("Штапик")
materials.append("Фурнитура")

# Удаление второго элемента
del materials[1]

# Вывод итогового результата
print()
print("После изменений:")
print("Итоговый список:", materials)
print(f"Количество материалов: {len(materials)}")
