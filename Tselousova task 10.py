# Задача 10 (Целоусова)
wh = {"Краска":{"q":2800,"p":850,"min":600},"Лак":{"q":95,"p":1320,"min":40},"Грунтовка":{"q":22,"p":550,"min":30},"Антисептик":{"q":50,"p":680,"min":25},"Морилка":{"q":120,"p":420,"min":50}}
total=0; mx_n=""; mx_v=0
print("=== Целоусова СКЛАД ===")
for n,d in wh.items():
    c=d["q"]*d["p"]
    total+=c
    if c>mx_v: mx_v,mx_n=c,n
    print(f"{n}: {d['q']}*{d['p']}={c} (min {d['min']})")
print(f"Всего: {total}, Макс: {mx_n} ({mx_v})")
