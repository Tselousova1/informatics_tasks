# Задача 8. Анализ заказов
# Программа анализирует материалы трёх подрядчиков
# с помощью множеств.

# Списки материалов трёх подрядчиков
contractor_1 = ["Стекло", "Профиль", "Стеклопакет", "Фурнитура"]
contractor_2 = ["Профиль", "Штапик", "Стеклопакет", "Уплотнитель"]
contractor_3 = ["Стеклопакет", "Профиль", "Герметик", "Фурнитура"]

# Преобразование списков в множества
set_1 = set(contractor_1)
set_2 = set(contractor_2)
set_3 = set(contractor_3)

# Все уникальные материалы
all_unique = set_1 | set_2 | set_3

# Общие материалы для всех подрядчиков
common_all = set_1 & set_2 & set_3

# Материалы только у первого подрядчика
only_first = set_1 - set_2 - set_3

# Материалы, которые есть ровно у двух подрядчиков
only_two = (set_1 & set_2) | (set_1 & set_3) | (set_2 & set_3)
only_two = only_two - common_all

# Вывод результата
print("=== АНАЛИЗ ЗАКАЗОВ ===")
print(f"Все уникальные материалы: {all_unique}")
print(f"Общие для всех подрядчиков: {common_all}")
print(f"Только у первого подрядчика: {only_first}")
print(f"Есть ровно у двух подрядчиков: {only_two}")
