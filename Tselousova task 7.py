# Задача 7. Прайс-лист материалов
# Программа выполняет основные операции со словарём:
# добавление, изменение, удаление элементов и расчёт средней цены.

# Исходный словарь материалов и цен
prices = {
    "Стекло": 600,
    "Зеркало": 1000,
    "Стеклопакет": 3000,
    "Профиль": 800,
    "Уплотнитель": 200
}

# Вывод исходного словаря
print("=== ПРАЙС-ЛИСТ МАТЕРИАЛОВ ===")
print("Исходный словарь:")
for material, price in prices.items():
    print(f"{material}: {price:.2f} руб.")

# Добавление новых материалов
prices["Штапик"] = 100
prices["Фурнитура"] = 5000

# Изменение цены одного материала на 10%
prices["Стеклопакет"] = prices["Стеклопакет"] * 1.10

# Удаление одного материала
del prices["Уплотнитель"]

# Расчёт средней цены
average_price = sum(prices.values()) / len(prices)

# Вывод итогового результата
print()
print("После изменений:")
for material, price in prices.items():
    print(f"{material}: {price:.2f} руб.")
print(f"Средняя цена: {average_price:.2f} руб.")
